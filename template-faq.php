						<h3>¿Qué es una base?</h3> 
						<p>Las Bases de PreGel son ingredientes esenciales en polvo perfectamente equilibrados.  
							Las mismas tienen como función principal el optimizar el sabor y textura del gelato, así como facilitar su producción.  La Base es fundamental para crear una estructura y textura que permitan la cremosidad y suavidad deseadas de las que se enorgullece PreGel.</p>

							<h3>¿Qué es un estabilizante?</h3>
							<p>Existen dos tipos de estabilizantes utilizados en el gelato. Uno proviene de fuentes animales y el más comúnmente utilizado es la gelatina. El otro estabilizante es una fuente vegetal como carragenano, goma de guar y Goma de garrofín. Los estabilizantes se utilizan en el gelato por una variedad de razones:  incrementan la viscosidad, mejoran la suavidad del cuerpo del producto e impiden que se formen cristales de hielo y que se derrita.</p>

							<h3>¿Qué es el Arabeschi®?</h3> 
							<p>El Arabeschi® es un topping y relleno para el gelato. Como topping se utiliza para decorar y hacer más llamativo el gelato en tarrinas y bandejas, así como los semifreddo. También se emplea como relleno para darle textura y sabor adicional al gelato. Entre algunos de nuestros mejores Arabeschi® se encuentran el Pino Pinguino®, el Crocante de Café y diferentes tipos de Frutas Confitadas.</p>

							<h3>¿Cuáles son los ingredientes básicos para elaborar gelato?</h3> 
							<p>En la explicación más básica, el gelato se hace mezclando leche o agua, azúcar y otros ingredientes naturales (como emulsionantes y estabilizantes) que integran la base, luego se agregan nuestros distintivos sabores de crema o fruta.</p>

							<h3>¿Cuál es la diferencia entre el proceso frío y el caliente?</h3>
							<p>El proceso caliente requiere de pasteurización, mientras que el proceso frío no. Para aprender acerca de estos procesos, lo invitamos a asistir a una de nuestras clases de capacitación.</p> 

							<h3>¿Cuál es la cantidad de saborizante que debo utilizar?</h3>
							<p>Todo depende de la receta que elabore, cada saborizante tienen instrucciones en su empaque de una receta estándar. Si tiene cualquier pregunta no dude en consultar a su Asesor Comercial o nuestro Departamento de Servicio al Cliente.</p>

							<h3>¿Durante cuánto tiempo puedo guardar mi gelato en la vitrina?</h3>
							<p>Esta respuesta variará ligeramente dependiendo de diferentes factores durante la preparación, pero por lo general entre 3 y 5 días, siempre y cuando guarde su gelato en el congelador por la noche. Quizá consiga también una mayor vida útil si prepara su gelato con el proceso caliente. Tenga en cuenta que la forma preferida de servir el gelato es cuando se encuentra en su estado más fresco.</p>

							<h3>¿Cuál es la temperatura para el almacenaje?</h3> 
							<p>La temperatura para almacenar en el congelador debe de ser de aproximadamente -17.7 grados centígrados.</p>

							<h3>¿A qué temperatura tiene que estar el gelato en cuanto sale del mantecador?</h3> 
							<p>Entre -7.7 y -5 grados centígrados. En este punto, sólo entre el 50% y el 60% del agua está congelada.</p>

							<h3>¿Cómo se presenta el gelato?</h3> 
							<p>Por lo general en contenedores de 5 litros. Sale del congelador discontinuo fácilmente, sin necesidad de presionar o empujarlo.  El resultado debe ser armonioso con muchas ondas y rizos.</p>

							<h3>¿Cómo se sirve el gelato?</h3> 
							<p>Aunque con el helado se suelen utilizar palas, el gelato se sirve con espátulas, que le permiten adoptar una forma más artesanal.</p>

							<h3>¿Decoración del gelato?</h3> 
							<p>Sin duda, el gelato es considerado un postre artesanal, lo cual significa que la decoración y presentación del mismo son tan importantes como su sabor y textura. Dichos elementos forman parte esencial de la experiencia integral de disfrutar un gelato. El Arabeschi® y los Toppings de PreGel le agregan no solo sabor y textura al gelato, sino también lo hacen más apetitivos a la vista del consumidor.</p>  

							<h3>¿Durante cuánto tiempo durarán mis productos PreGel?</h3> 
							<p>La mayoría de los productos PreGel tienen una vida útil de tres años a partir del momento en que son producidos; el que se hayan abierto no afecta su vida útil. Sin embargo, recuerde guardar los productos bien sellados y no los almacene en temperaturas extremas, esto puede acortar su vida útil. La fecha de caducidad se indica en el empaque en la forma europea, día/mes/año. ¿ Cuánto tiempo durarán mis productos? Atencion a Clientes-Servicio al Cliente.</p>

							<h3>¿Qué debo hacer cuando el aceite se separa en las Pastas, Arabeschi® o los Toppings?</h3>
							<p>Simplemente mézclelos, en pocos segundos recuperaran su textura original. Y agite los toppings. Le recomendamos los mezcle de vez en cuando para evitar la separación.</p> 

							<h3>¿Cuál es el contenido nutricional de mi producto?</h3> 
							<p>PreGel sólo puede proporcionar información nutricional sobre los productos no acabados que produce. Si quiere evaluar los contenidos nutricionales del producto acabado que usted elabora, le recomendamos que se asocie con un laboratorio local de pruebas que pueda proporcionarle con exactitud la información nutricional sobre su producto.</p>

